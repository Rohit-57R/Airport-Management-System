-- 1. SAME QUERY USING JOINS (OPTIMIZED VERSION)

SELECT P.FIRST_NAME, P.LAST_NAME, A.NAME
FROM PASSENGERS P
JOIN BOOKING B ON P.PASSENGER_ID = B.PASSENGER_ID
JOIN TICKETS T ON B.BOOKING_ID = T.BOOKING_ID
JOIN DAILY_FLIGHT_STATUS D ON T.DAILY_FLIGHT_ID = D.DAILY_FLIGHT_ID
JOIN FLIGHT_SCHEDULE FS ON D.SCHEDULE_ID = FS.SCHEDULE_ID
JOIN FLIGHTS F ON FS.FLIGHT_ID = F.FLIGHT_ID
JOIN AIRPORTS A ON F.DESTINATION_ID = A.AIRPORT_ID
WHERE P.PHONE_NUMBER = 9876543211;


-- 2. TOTAL BOOKINGS PER AIRLINE

SELECT A.NAME, COUNT(*) TOTAL_BOOKINGS
FROM AIRLINES A
JOIN FLIGHTS F ON A.AIRLINE_ID = F.AIRLINE_ID
JOIN FLIGHT_SCHEDULE FS ON F.FLIGHT_ID = FS.FLIGHT_ID
JOIN DAILY_FLIGHT_STATUS D ON FS.SCHEDULE_ID = D.SCHEDULE_ID
JOIN TICKETS T ON D.DAILY_FLIGHT_ID = T.DAILY_FLIGHT_ID
GROUP BY A.NAME;


-- 3. TOTAL REVENUE FROM CONFIRMED BOOKINGS

SELECT SUM(TOTAL_AMOUNT) TOTAL_REVENUE
FROM BOOKING
WHERE STATUS = 'Confirmed';


-- 4. MOST ACTIVE AIRPORT (BY FLIGHTS)

SELECT A.NAME, COUNT(*) TOTAL_FLIGHTS
FROM AIRPORTS A
JOIN FLIGHTS F ON A.AIRPORT_ID = F.ORIGIN_ID
GROUP BY A.NAME
ORDER BY TOTAL_FLIGHTS DESC;


-- 5. PASSENGERS WITH MULTIPLE BOOKINGS

SELECT PASSENGER_ID, COUNT(*) BOOKINGS
FROM BOOKING
GROUP BY PASSENGER_ID
HAVING COUNT(*) > 1;


-- 6. DELAYED FLIGHTS

SELECT *
FROM DAILY_FLIGHT_STATUS
WHERE STATUS = 'Delayed';


-- 7. CREW ASSIGNED TO EACH FLIGHT

SELECT C.FIRST_NAME, C.LAST_NAME, D.DAILY_FLIGHT_ID
FROM CREW C
JOIN CREW_ASSIGNMENTS CA ON C.CREW_ID = CA.CREW_ID
JOIN DAILY_FLIGHT_STATUS D ON CA.DAILY_FLIGHT_ID = D.DAILY_FLIGHT_ID;


-- 8. PASSENGER WITH SEAT NUMBER AND CLASS

SELECT P.FIRST_NAME, T.SEAT_NUMBER, T.TICKET_CLASS
FROM PASSENGERS P
JOIN BOOKING B ON P.PASSENGER_ID = B.PASSENGER_ID
JOIN TICKETS T ON B.BOOKING_ID = T.BOOKING_ID;


-- 9. BAGGAGE STATUS PER PASSENGER

SELECT P.FIRST_NAME, BG.STATUS
FROM PASSENGERS P
JOIN BOOKING B ON P.PASSENGER_ID = B.PASSENGER_ID
JOIN TICKETS T ON B.BOOKING_ID = T.BOOKING_ID
JOIN BAGGAGE BG ON T.TICKET_ID = BG.TICKET_ID;


-- 10. AIRCRAFT USED IN MAXIMUM FLIGHTS

SELECT AIRCRAFT_ID, COUNT(*) TOTAL
FROM FLIGHTS
GROUP BY AIRCRAFT_ID
HAVING COUNT(*) = (
SELECT MAX(COUNT(*))
FROM FLIGHTS
GROUP BY AIRCRAFT_ID
);

-- 11. TOP 3 AIRLINES BASED ON NUMBER OF FLIGHTS

SELECT NAME, TOTAL_FLIGHTS
FROM (
    SELECT A.NAME, COUNT(*) TOTAL_FLIGHTS,
           RANK() OVER (ORDER BY COUNT(*) DESC) RANK_NO
    FROM AIRLINES A
    JOIN FLIGHTS F ON A.AIRLINE_ID = F.AIRLINE_ID
    GROUP BY A.NAME
)
WHERE RANK_NO <= 3;


-- 12. PASSENGERS WHO TRAVELLED IN BUSINESS CLASS

SELECT DISTINCT P.FIRST_NAME, P.LAST_NAME
FROM PASSENGERS P
JOIN BOOKING B ON P.PASSENGER_ID = B.PASSENGER_ID
JOIN TICKETS T ON B.BOOKING_ID = T.BOOKING_ID
WHERE T.TICKET_CLASS = 'Business';


-- 13. COUNT OF FLIGHTS PER DAY OF WEEK

SELECT DAYS_OF_WEEK, COUNT(*) TOTAL_FLIGHTS
FROM FLIGHT_SCHEDULE
GROUP BY DAYS_OF_WEEK;


-- 14. FIND PASSENGERS WHO HAVE NOT BOOKED ANY TICKETS

SELECT *
FROM PASSENGERS
WHERE PASSENGER_ID NOT IN (
    SELECT PASSENGER_ID FROM BOOKING
);


-- 15. FIND AIRCRAFT WHICH NEVER USED IN FLIGHTS

SELECT *
FROM AIRCRAFT
WHERE AIRCRAFT_ID NOT IN (
    SELECT AIRCRAFT_ID FROM FLIGHTS
);
