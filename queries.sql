-- 1. DISPLAY THE AIRPORT NAME, IT MUST BE DESTINATION OF PASSENGER, WITH THE PASSENGER MOBILE NUMBER 9876543211.

SELECT NAME
FROM AIRPORTS
WHERE AIRPORT_ID IN(SELECT DESTINATION_ID
FROM FLIGHTS
WHERE FLIGHT_ID IN(SELECT FLIGHT_ID
FROM FLIGHT_SCHEDULE
WHERE SCHEDULE_ID IN (SELECT SCHEDULE_ID
FROM DAILY_FLIGHT_STATUS
WHERE DAILY_FLIGHT_ID IN (SELECT DAILY_FLIGHT_ID
FROM TICKETS
WHERE BOOKING_ID IN(SELECT BOOKING_ID
FROM BOOKING
WHERE PASSENGER_ID IN (SELECT PASSENGER_ID
FROM PASSENGERS
WHERE PHONE_NUMBER = 9876543211))))));


-- 2. DISPLAY PASSENGER DETAILS WHO LANDED ON HYDERABAD AIRPORT ON 25-JAN-2026

SELECT *
FROM PASSENGERS
WHERE PASSENGER_ID IN (SELECT PASSENGER_ID
FROM BOOKING
WHERE BOOKING_ID IN (SELECT BOOKING_ID
FROM TICKETS
WHERE DAILY_FLIGHT_ID IN (SELECT DAILY_FLIGHT_ID
FROM DAILY_FLIGHT_STATUS
WHERE FLIGHT_DATE = '25-JAN-2026'AND SCHEDULE_ID IN (SELECT SCHEDULE_ID
FROM FLIGHT_SCHEDULE
WHERE FLIGHT_ID IN (SELECT FLIGHT_ID
FROM FLIGHTS
WHERE DESTINATION_ID IN (SELECT AIRPORT_ID
FROM AIRPORTS
WHERE CITY = 'Hyderabad'))))));


-- 3. FIND MODEL AND CAPACITY OF THE AIRCRAFT WHICH HAS HIGHEST CAPACITY

SELECT MODEL, CAPACITY
FROM AIRCRAFT
WHERE CAPACITY IN(SELECT MAX(CAPACITY)
FROM AIRCRAFT);

-- 4. NAME OF THE AIRPORT USED AS ORIGIN BUT NEVER AS DESTINATION

SELECT NAME
FROM AIRPORTS
WHERE AIRPORT_ID IN (SELECT ORIGIN_ID
FROM FLIGHTS)AND AIRPORT_ID NOT IN (SELECT DESTINATION_ID
FROM FLIGHTS
);


-- 5. THE AIRLINE NAME OPERATING THE MAXIMUM NUMBER OF FLIGHTS

SELECT NAME
FROM AIRLINES
WHERE AIRLINE_ID = (SELECT AIRLINE_ID
FROM FLIGHTS
GROUP BY AIRLINE_ID
HAVING COUNT(*) = (SELECT MAX(COUNT(*))
FROM FLIGHTS
GROUP BY AIRLINE_ID));


-- 6. FIND THE AIRPORT NAME THAT HAS NO SCHEDULE FLIGHTS

SELECT NAME
FROM AIRPORTS
WHERE AIRPORT_ID NOT IN (
SELECT ORIGIN_ID FROM FLIGHTS
UNION
SELECT DESTINATION_ID FROM FLIGHTS
);


